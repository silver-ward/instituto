//soma
const somar = (x, y) => {
    return x + y;
}
//subtracao
const subtrair = (x, y) => {
    return x - y;
}
//divisao
const dividir = (x, y) => {
    return x / y;
}
//multiplicacao
const multiplicar = (x, y) => {
    return x * y;
}

module.exports = {
    somar,
    subtrair,
    dividir,
    multiplicar}